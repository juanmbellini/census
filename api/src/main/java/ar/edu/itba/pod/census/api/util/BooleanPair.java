package ar.edu.itba.pod.census.api.util;

/**
 * A {@link Pair} of {@link Boolean}.
 */
public class BooleanPair extends Pair<Boolean, Boolean> {

    /**
     * Constructor.
     *
     * @param l The left {@link Boolean} of the {@link Pair}.
     * @param r The right {@link Boolean} of the {@link Pair}.
     */
    public BooleanPair(Boolean l, Boolean r) {
        super(l, r);
    }
}
