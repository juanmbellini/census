package ar.edu.itba.pod.census.api.util;

/**
 * A {@link Pair} of {@link String}.
 */
public class StringPair extends Pair<String, String> {

    /**
     * Constructor.
     *
     * @param l The left {@link String} of the {@link Pair}.
     * @param r The right {@link String} of the {@link Pair}.
     */
    public StringPair(String l, String r) {
        super(l, r);
    }
}
