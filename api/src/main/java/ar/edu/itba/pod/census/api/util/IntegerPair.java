package ar.edu.itba.pod.census.api.util;

/**
 * A {@link Pair} of {@link Integer}.
 */
public class IntegerPair extends Pair<Integer, Integer> {

    /**
     * Constructor.
     *
     * @param l The left {@link Integer} of the {@link Pair}.
     * @param r The right {@link Integer} of the {@link Pair}.
     */
    public IntegerPair(Integer l, Integer r) {
        super(l, r);
    }
}
